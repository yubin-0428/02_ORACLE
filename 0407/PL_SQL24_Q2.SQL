SET SERVEROUTPUT ON;
DECLARE
   V_DEPTNO DEPT.DEPTNO%TYPE := 60;
BEGIN

	
	CASE V_DEPTNO
		WHEN 10 THEN 
		 DBMS_OUTPUT.PUT_LINE('dname: ACCOUNTING');
		WHEN 20 THEN 
		 DBMS_OUTPUT.PUT_LINE('dname: RESEARCH');
		WHEN 30 THEN 
		 DBMS_OUTPUT.PUT_LINE('dname: SALES');
		WHEN 40 THEN 
		 DBMS_OUTPUT.PUT_LINE('dname: OPERATIONS');
        ELSE 
         DBMS_OUTPUT.PUT_LINE('dname: N/A');
    END CASE;

END;
